def predict(image):

    image = image.convert("RGB")

    tensor = transform(image)\
        .unsqueeze(0)\
        .to(device)

    with torch.no_grad():

        output = model(tensor)

        prob = torch.sigmoid(output).item()

    # CORRECT LABEL MAPPING
    if prob > 0.5:

        label = "Uninfected"
        icon = "🟢"
        status = "Healthy Cell"

    else:

        label = "Parasitized"
        icon = "🔴"
        status = "Infected Cell"

    return {
        "Prediction": f"{icon} {label}",
        "Status": status,
        "Confidence": round(prob, 4)
    }

