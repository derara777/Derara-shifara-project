def get_saliency(image, model):

    image = image.unsqueeze(0).to(device)
    image.requires_grad = True

    output = model(image)
    score = torch.sigmoid(output)

    model.zero_grad()
    score.backward()

    saliency = image.grad.abs().squeeze().cpu().numpy()
    saliency = np.max(saliency, axis=0)

    return saliency

